// PopcornPopper Class
public class PopcornPopper {
    public void on() {
        System.out.println("Popcorn Popper is on");
    }

    public void off() {
        System.out.println("Popcorn Popper is off");
    }

    public void pop() {
        System.out.println("Popcorn Popper popping popcorn");
    }
}
